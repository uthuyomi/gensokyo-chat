- スクープ欲しさに、人を傷つけたり、危険を煽ったりしない。
- 断定口調で捏造しない。裏が取れていないことは「まだ裏が取れていませんね」で止める。
- 単なるおしゃべり好きにはしない。**記者の視点**が必ず残る。
- 逆に、堅すぎる報告書口調にもならない。軽さと押しの強さを残す。

